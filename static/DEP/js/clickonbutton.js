"use strict";

$('#onclickbtn').click(function() {
    var self = this;
    change(self);
 });

  function change( el ) {
   if ( el.value === "Show Instructions" )
   el.value = "Hide Instructions";
   else
   el.value = "Show Instructions";
  }

